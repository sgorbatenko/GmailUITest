
package com.stan.task.framework.exception;

public class NoSuchAttributeException extends TestAutomationException
{

    /**
     * 
     */
    private static final long serialVersionUID = 1401689349019401960L;

    public NoSuchAttributeException()
    {
    }

    public NoSuchAttributeException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public NoSuchAttributeException(String message)
    {
        super(message);
    }

    public NoSuchAttributeException(Throwable cause)
    {
        super(cause);
    }

}
