
package com.stan.task.test.framework.exception;


public class CoreLevelException extends TestAutomationException
{
    private static final long serialVersionUID = 2186161538996276667L;

    public CoreLevelException()
    {
        super();
    }

    public CoreLevelException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public CoreLevelException(String message)
    {
        super(message);
    }

    public CoreLevelException(Throwable cause)
    {
        super(cause);
    }
}
