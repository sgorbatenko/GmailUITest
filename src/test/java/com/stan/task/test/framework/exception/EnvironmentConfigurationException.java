
package com.stan.task.test.framework.exception;

public class EnvironmentConfigurationException extends Exception
{

    /**
     * 
     */
    private static final long serialVersionUID = 1990318023708697990L;

    public EnvironmentConfigurationException()
    {
        super();
    }

    public EnvironmentConfigurationException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public EnvironmentConfigurationException(String message)
    {
        super(message);
    }

    public EnvironmentConfigurationException(Throwable cause)
    {
        super(cause);
    }


}
